# If statements allow code to be run based on conditions
# The code in the if statment will only be run if the test returns True
# Note the colon and the indent

num = 10

if (num == 10): # returns True
    print('The number is ten') # this code will run

if (num > 10): # returns False
    print('The number is greater than ten') #this code won't
