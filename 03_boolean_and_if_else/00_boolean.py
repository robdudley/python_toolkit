# Boolean operations allow us to test conditions in our code
# And returns True or False

# We test for equality using a double equal sign
1 == 1 # returns True

'Cat' == 'Dog' # returns False

# We test for inequality using !=
1 != 2 # returns True

'Fish' != 'Carp' # returns True

# We can test numeric data for greater than or less than
10 > 5 # returns True

20 < 10 # returns False

# We can test for greater than or equal to / less than or equal to

11 <= 11 # returns True

23 >= 14 # returns True
