# elif lets you chain multiple tests together to save writing lots of if statements
# only the first test returning true will be executed

animal = 'dog'

if (animal == 'cat'):
    print('I like cats')
elif (animal == 'dog'):
    print('I love dogs')
elif (animal == 'goldfish'):
    print('I quite like goldfish')
else:
    print('I do not know what that is.')
