# Python Toolkit
# variables_and_types/types

# Python supports lots of different types of data
# We can see the type of a value using the type() function
# We can also convert some variables to other types
# For more info on types see:
# https://www.tutorialspoint.com/python/python_variable_types.htm


type(True) # True is a Boolean

type(42) # 42 is an integer or whole number

type(1.234) # 1.234 is a floating point number or float

type('Hello World') # this is a string

type("Hello World") # this is also a string

type('42') # this is a string

# Type Casting / Conversion
int('42') # the int function converts a string to an integer

str(23) # the str function converts a number (integer or float) to a string
