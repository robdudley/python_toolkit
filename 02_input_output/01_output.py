# We can make out programs output to the console using print
print('This will be output to the console')

# We can output multiple statements seperated by commas
print('Richard', 'of', 'York', 'grimly', 'battled', 'in', 'vain')

# Note that the above includes a space between every word. We can remove this
# by specifying a "sep" argument
print('Hello ', 'World', '!', sep='')
