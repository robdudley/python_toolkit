# Python Toolkit
# loops/for

# A for loop is used to be loop over a range or list of items
# For more on for loops see:
# https://www.tutorialspoint.com/python/python_for_loop.htm

# This loop prints each letter in turn

for letter in 'Python':
    print('Current Letter :', letter)

# This loop prints the numbers from 10 to 20

for num in range(10,20):
    print(num)
